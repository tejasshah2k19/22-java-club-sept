
public class CastingDemo {

	public static void main(String[] args) {

		// primitive casting
		int a = 5;
		int b = 2;
		float c = (float) a / b;// 2.5 -> 2 -> int/int -> float->int
		// casting --> upcast downcast
		// downcast -> value loss
		// implicit cast
		// explicit cast
		System.out.println(c);

		// NonPrimitive casting ---> class

	}
}


